;---------------------------------------------------------
;---------------------------------------------------------
;- Static properties operations / Class defined
;---------------------------------------------------------
;---------------------------------------------------------

;---------------------------------------------------------
; Get Static Property / Class defined
;---------------------------------------------------------
; dest = A, BC, DE, HL, IX, IY, SP
;---------------------------------------------------------
	.macro GetStClassProp dest, class, prop
	ld dest, (#class'.sprop.'prop)
	.endm

;---------------------------------------------------------
; Get Static Property Pointer / Class defined
;---------------------------------------------------------
; dest = BC, DE, HL, IX, IY, SP
;---------------------------------------------------------
	.macro GetStClassPropPtr dest, class, prop
	ld dest, #class'.sprop.'prop'
	.endm

;---------------------------------------------------------
; Set Static Property / Class defined
;---------------------------------------------------------
; src = A, BC, DE, HL, IX, IY, SP
;---------------------------------------------------------
	.macro SetStClassProp class, prop, src
	ld (#class'.sprop.'prop), src
	.endm

